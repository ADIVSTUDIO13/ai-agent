export function getRandomUserAgent() {
  return USER_AGENTS[Math.floor(Math.random() * USER_AGENTS.length)];
}

export function getRandomIP() {
  const r = () => Math.floor(Math.random() * 254) + 1;
  // Exclude private IP ranges by re-rolling first octet if needed
  let first = r();
  while (first === 10 || first === 172 || first === 192 || first === 127) {
    first = r();
  }
  return `${first}.${r()}.${r()}.${r()}`;
}

export function getBypassHeaders(targetDomain = '') {
  const ip = getRandomIP();
  const ua = getRandomUserAgent();
  const headers = {
    'User-Agent': ua,
    'X-Forwarded-For': ip,
    'X-Real-IP': ip,
    'Client-IP': ip,
    'X-Client-IP': ip,
    'VIA': `1.1 ${ip}`,
    'Accept-Language': 'id-ID,id;q=0.9,en-US;q=0.8,en;q=0.7',
    'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8',
    'Cache-Control': 'no-cache',
    'Pragma': 'no-cache',
    'Connection': 'keep-alive',
    'Upgrade-Insecure-Requests': '1'
  };

  if (targetDomain) {
    headers['Origin'] = `https://${targetDomain}`;
    headers['Referer'] = `https://${targetDomain}/`;
    headers['Host'] = targetDomain;
  }
  return headers;
}
